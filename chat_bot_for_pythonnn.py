import random

print("🤖 Бот-ассистент запущен и готов к работе! 🚀")
print("(Чтобы выйти, напишите 'пока' или 'выход')\n" + "-"*40)

while True:
    answer = input('👤 Вы: ').lower().strip()
    
    # Команда для выхода из бота
    if 'пока' in answer or 'выход' in answer or 'стоп' in answer:
        print('🤖 Бот: До свидания, хозяин! Возвращайтесь скорее! 👋✨')
        break
        
    elif 'прив' in answer or 'здрав' in answer:
        print('🤖 Бот: Здравствуйте, хозяин! Рад вас приветствовать! 👑🤖')
        
    elif 'как' in answer:
        print('🤖 Бот: У меня всё отлично, системы работают на 100%! ⚡ А как ваши дела? 😊')
        
    elif 'что' in answer:
        print('🤖 Бот: Не покладая процессора, работаю исключительно на благо вас! 🛠️💻')
        
    elif 'шут' in answer or 'анекдот' in answer:
        # Добавим немного разнообразия в шутки
        jokes = [
            'Колобок повесился! 😹',
            'Штирлиц шёл по улице и упал в лужу. Чудо — подумал Штирлиц. Лужа — подумало чудо. 🕵️‍♂️',
            'Программисты не плачут, они удаляют слезы из кода. 🥲💻'
        ]
        print(f'🤖 Бот: {random.choice(jokes)}')
        
    elif 'фильм' in answer or 'кино' in answer:
        genre = input('🤖 Бот: Отличный выбор! Какой жанр вас интересует? 🍿🎬\n👤 Вы: ').lower().strip()
        
        if 'боевик' in genre:
            print('🤖 Бот: Рекомендую безумный экшен: "Форсаж" или "Джон Уик"! 🚗💨💥')
        elif 'драм' in genre: # Убрали лишнюю 'м', теперь сработает и на 'драма', и на 'драмма'
            print('🤖 Бот: Классика на все времена: легендарный "Титаник"! 🚢💔')
        elif 'комед' in genre:
            print('🤖 Бот: Чтобы посмеяться, гляньте "Один дома" или "Мальчишник в Вегасе"! 😹🍿')
        else:
            print('🤖 Бот: Ой, такого жанра в моей базе пока нет... Но я обязательно его выучу! 📚🤖')
            
    else:
        print('🤖 Бот: Извините, хозяин, я вас не совсем понял... 🤖❓ Попробуйте перефразировать!')
        
    print("-" * 40) # Визуальный разделитель для удобства чтения в консоли

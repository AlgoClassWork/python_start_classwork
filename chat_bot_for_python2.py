from random import choice, randint

print('🤖 Я чат-бот Олег. Чем могу помочь?')

while True:
    answer = input('👉 Чего желаете? ').lower()

    if 'привет' in answer or 'здравствуй' in answer:
        greetings = [
            'Приветствую, хозяин! 😎 Как жизнь?',
            'О, вы вернулись! Рад вас видеть 😄',
            'Здравия желаю! Чем займёмся сегодня? 🚀'
        ]
        print(choice(greetings))

    elif 'как дела' in answer or 'как ты' in answer:
        print(choice([
            'У меня всё отлично 😎 Я же программа!',
            'Работаю без выходных 🤖 А вы как?',
            'Лучше всех! Ни налогов, ни проблем 😄'
        ]))

    elif 'что делаешь' in answer:
        print(choice([
            'Думаю о смысле жизни... 0 и 1 🤯',
            'Жду ваших команд, хозяин 😎',
            'Смотрю на код и делаю вид, что понимаю 😅'
        ]))

    elif 'шут' in answer or 'анекдот' in answer:
        jokes = [
            'Почему программисты путают Хэллоуин и Рождество? Потому что OCT 31 == DEC 25 🎃🎄',
            'Я не ленивый, я просто в режиме энергосбережения 😴',
            'Баг — это не ошибка, это неожиданная фича 😏',
            'Сначала было слово, потом баг... 🐛',
            'Почему компьютер холодный? Потому что у него Windows открыты 🪟❄️'
        ]
        print(choice(jokes))

    elif 'фильм' in answer:
        genre = input('🎬 Какой жанр вас интересует? ').lower()

        if 'боевик' in genre:
            films = ['Джон Уик', 'Мстители', 'Безумный Макс', 'Форсаж']
            print(f'🔥 Советую: {choice(films)}')

        elif 'комедия' in genre:
            films = ['Маска', 'Один дома', 'Тупой и ещё тупее', 'Мы — Миллеры']
            print(f'😂 Попробуйте: {choice(films)}')

        elif 'ужас' in genre or 'хоррор' in genre:
            films = ['Оно', 'Заклятие', 'Астрал', 'Пила']
            print(f'👻 На свой страх и риск: {choice(films)}')

        elif 'фантастика' in genre:
            films = ['Интерстеллар', 'Матрица', 'Начало', 'Аватар']
            print(f'🚀 Отличный выбор: {choice(films)}')

        elif 'мульт' in genre:
            films = ['Шрек', 'Король Лев', 'Холодное сердце', 'Мадагаскар']
            print(f'🐸 Советую: {choice(films)}')

        else:
            print('🤔 Хм... Я пока не знаю такого жанра, но скоро выучу!')

    elif 'совет' in answer:
        tips = [
            'Никогда не откладывай на завтра то, что можно сделать... послезавтра 😄',
            'Пей больше воды 💧 (даже если ты не бот)',
            'Не доверяй людям, которые не любят котиков 🐱',
            'Сохраняй код чаще! Это вопрос жизни 😱'
        ]
        print(choice(tips))

    elif 'игра' in answer or 'угадай' in answer:
        print('🎮 Игра "Угадай число" начинается!')
        number = randint(1, 100)
        attempts = 0

        print('Я загадал число от 1 до 100 😏 Попробуй угадать!')

        while True:
            guess = input('👉 Твой вариант: ')

            if not guess.isdigit():
                print('😅 Введи число!')
                continue

            guess = int(guess)
            attempts += 1

            if guess < number:
                print('📉 Слишком маленькое!')
            elif guess > number:
                print('📈 Слишком большое!')
            else:
                print(f'🎉 Угадал! Это было число {number}!')
                print(f'🧠 Попыток: {attempts}')
                
                print(choice([
                    '😎 Неплохо! Ты явно не случайный игрок',
                    '🔥 Да ты просто гений!',
                    '🤖 Хм... ты точно не бот?'
                ]))
                break

    elif 'пока' in answer or 'выход' in answer:
        print('👋 Буду ждать вас снова, хозяин!')
        break

    else:
        print(choice([
            '🤖 Я вас не понял... попробуйте иначе',
            '😅 Моя не понимать, хозяин',
            '❓ Интересно... но я пока не знаю ответа'
        ]))

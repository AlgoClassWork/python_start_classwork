from random import choice, randint

greetings = [
    'Привет, хозяин! 😎',
    'О, вы вернулись! 👀',
    'Здравствуйте! Чем займёмся сегодня? 🚀',
    'Привет-привет! 😊'
]

how_are_you = [
    'Отлично! Всегда готов служить 😏',
    'Работаю как часы ⏰ А вы как?',
    'Лучше всех! 😎 А у вас как дела?',
    'Нормально, не жалуюсь 🤖'
]

what_doing = [
    'Работаю на вас 💼',
    'Жду ваших команд 👀',
    'Думаю о смысле жизни... шучу 😄',
    'Помогаю вам становиться круче 😎'
]

jokes = [
    'Колобок повесился 😂',
    'Русалка села на шпагат 🤸‍♀️',
    'Рыба утонула 🐟',
    'Почему программисты не любят природу? Там слишком много багов 🐛😂',
    'Я не ленивый, я в режиме энергосбережения 🔋😴',
    '404: Шутка не найдена 🤖'
]

films = {
    'ужасы': ['Оно 🎈', 'Астрал 👻', 'Синистер 😱', 'Заклятие 🕯️'],
    'комедия': ['1+1 😂', 'Мальчишник в Вегасе 🍻', 'Очень плохая училка 😏'],
    'боевик': ['Форсаж 🚗💨', 'Джон Уик 🔫', 'Рэмбо 💣'],
    'фантастика': ['Интерстеллар 🌌', 'Матрица 🧠', 'Аватар 🌍'],
    'драма': ['Зелёная миля 💔', 'Побег из Шоушенка 🔓', 'Форрест Гамп 🏃'],
    'мультфильм': ['Шрек 🐸', 'Король Лев 🦁', 'Тачки 🚗']
}

unknown = [
    'Хмм… я такого не знаю 🤔',
    'Попробуйте сказать по-другому 😅',
    'Ошибка понимания… но я учусь! 🤖',
    'Интересно… но непонятно 😄'
]

bye = [
    'Буду ждать вас, хозяин 😌',
    'До встречи! 👋',
    'Возвращайтесь скорее 😎',
    'Пока-пока! 🖐️'
]

while True:
    answer = input('Чего желаете? ').lower()

    if 'прив' in answer:
        print(choice(greetings))

    elif 'как' in answer:
        print(choice(how_are_you))

    elif 'что' in answer:
        print(choice(what_doing))

    elif 'шут' in answer:
        print(choice(jokes))

    elif 'фильм' in answer:
        genre = input('Какой жанр хотите? 🎬 ').lower()
        found = False

        for key in films:
            if key in genre:
                print('Советую посмотреть:', choice(films[key]))
                found = True
                break

        if not found:
            print('Не знаю такой жанр 😅 Попробуйте: ужасы, комедия, боевик, фантастика')

    elif 'игра' in answer or 'угадай' in answer:
        print('🎮 Давай сыграем в "Угадай число"!')
        number = randint(1, 1000)
        attempts = 0

        print('Я загадал число от 1 до 1000 😏 Попробуй угадать!')

        while True:
            guess = input('Твой вариант: ')

            if not guess.isdigit():
                print('Введи число 😅')
                continue

            guess = int(guess)
            attempts += 1

            if guess < number:
                print('Моё число больше 📈')
            elif guess > number:
                print('Моё число меньше 📉')
            else:
                print(f'🎉 Угадал! Это было число {number}')
                print(f'Ты справился за {attempts} попыток 😎')
                break

    elif 'помощь' in answer or 'help' in answer:
        print('''
Вот что я умею 😎:
- привет
- как дела
- что делаешь
- шутка
- фильм
- игра (угадай число)
- пока
''')

    elif 'пока' in answer:
        print(choice(bye))
        break

    else:
        print(choice(unknown))
